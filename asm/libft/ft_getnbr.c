int		ft_getnbr(char const *str)
{
	int		i;
	long	nb;
	int		neg;

	i = 0;
	nb = 0;
	neg = 1;
	while (str[i] != '\0' && (str[i] <= 32 || str[i] == 43 || str[i] == 45))
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i++] - '0');
		if ((nb > 2147483648 && neg == -1) || (nb > 2147483647 && neg == 1))
			return (0);
	}
	return (nb * neg);
}
