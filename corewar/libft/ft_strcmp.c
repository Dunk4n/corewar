int		ft_strcmp(char const *s1, char const *s2)
{
	char	*tmp1;
	char	*tmp2;

	tmp1 = (char*)s1;
	tmp2 = (char*)s2;
	while (*tmp1 != '\0' || *tmp2 != '\0')
	{
		if (*tmp1 != *tmp2)
			return (*tmp1 - *tmp2);
		++tmp1;
		++tmp2;
	}
	return (0);
}
